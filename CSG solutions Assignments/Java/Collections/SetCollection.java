package Java.Collections;

import java.util.HashSet;
import java.util.Set;

public class SetCollection {

    public static void main(String[] args) {
        Set <Integer> set = new HashSet<Integer>();
        set.add(6);
        set.add(4);
        set.add(2);
        set.add(10);


for(int i:set){
  System.out.println(i);
    }
    
    
}
}

